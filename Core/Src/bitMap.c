void drawBitmap(int x0, int y0, const Bitmap *bmp, uint8_t unused_fg)
{
    int width  = bmp->width;
    int height = bmp->height;

    const uint8_t *pixel_data = bmp->data;

    for (int y = 0; y < height; y++)
    {
        int screen_y = y0 + y;
        if (screen_y < 0 || screen_y >= VGA_DISPLAY_Y) continue;

        for (int x = 0; x < width; x++)
        {
            int screen_x = x0 + x;
            if (screen_x < 0 || screen_x >= VGA_DISPLAY_X) continue;

            uint8_t color = pixel_data[y * width + x];

            // 0 = transparant? (optioneel)
            // if (color == 0) continue;

            UB_VGA_SetPixel(screen_x, screen_y, color);
        }
    }
}
